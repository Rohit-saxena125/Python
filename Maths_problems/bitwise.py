# use Bitwise operator and or xor rightshift left shift not
num1,num2 = eval(input("Enter the digit:")),eval(input("Enter the digit:"))
print('-----------------------------------And---------------------------------------------------------')
print(num1 & num2)
print('-----------------------------------Or---------------------------------------------------------')
print(num1 | num2)
print('-----------------------------------Xor---------------------------------------------------------')
print(num1 ^ num2)
print('-----------------------------------Right shift---------------------------------------------------------')
print(num1 >> num2)
print('-----------------------------------Left shift---------------------------------------------------------')
print(num1 << num2)
print('-------------------------------------------Not-------------------------------------------------')
print(~num1)